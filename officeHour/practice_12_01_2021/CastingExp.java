public class CastingExp {

    public static void main(String[] args) {

        // double > float > long > int > short > byte

        double num=12;
        int num2=(int)num; //explicit casting  //done by developer

        short num4=34;
        int num5=num4; //implicit casting  //done by compiler

        byte num8=127;


    }
}
